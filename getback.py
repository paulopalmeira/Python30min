# FUNCTION
# é um bloco de código que realiza uma 
# tarefa específica. Ela é definida
# usando a palavra-chave "def" seguida
# pelo nome da função e parênteses que
# podem conter parâmetros (valores de
# entrada) necessários para a execução
# da função.

def anuncio(nome,sobrenome,idade):
    print("Olá, " +nome+" "+sobrenome)
    print("Você tem " + str(idade) + " anos.")
    print("Tenha um bom dia!")

anuncio("Paulo", "Palmeira", 41)