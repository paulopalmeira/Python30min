import math

pi = -3.14
x = 1
y = 2
z = 3

# o round vai arredondar
print(round(pi))

# o ceil para cima
print(math.ceil(pi))

# o floor para baixo
print(math.floor(pi))

# o abs vai dar o valor absoluto (se for negativo, ele positiva)
print(abs(pi))

# o pow eleva o n√∫mero ao quadrado
print(pow(pi,2))

# o sqrt calcula a raiz quadrada
print(math.sqrt(420))

#mostra o de maior valor  
print(max(x,y,z))

#mostra o de menor valor
print(min(x,y,z))
